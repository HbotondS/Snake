package com.project;

public class MyColor {
	private int RED;
	private int GREEN;
	private int BLUE;

	public MyColor(int red, int green, int blue) {
		RED = red;
		GREEN = green;
		BLUE = blue;
	}

	public int getRED() {
		return RED;
	}

	public void setRED(int RED) {
		this.RED = RED;
	}

	public int getGREEN() {
		return GREEN;
	}

	public void setGREEN(int GREEN) {
		this.GREEN = GREEN;
	}

	public int getBLUE() {
		return BLUE;
	}

	public void setBLUE(int BLUE) {
		this.BLUE = BLUE;
	}
}
